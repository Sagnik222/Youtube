// Default config for tools not in the list
const DEFAULT_CONFIG = {
  role: 'professionals',
  task: 'repetitive work',
  competitor: 'manual methods',
  expensive: 'agencies',
};

export function generateShortsScript(product) {
  const name = product.name;
  const tagline = product.tagline;
  const category = product.category;
  const features = product.features;
  const pricing = product.pricing;
  const audience = product.targetAudience;
  const affiliate = product.affiliateLink;

  // Find tool-specific config
  const configKey = Object.keys(TOOL_CONFIGS).find(k => name.toLowerCase().includes(k));
  const config = configKey ? TOOL_CONFIGS[configKey] : DEFAULT_CONFIG;

  const vars = {
    NAME: name,
    ROLE: config.role,
    TASK: config.task,
    COMPETITOR: config.competitor,
    EXPENSIVE: config.expensive,
    PRICE: 'FREE',
    NUMBER: String(Math.floor(Math.random() * 5 + 2)),
  };

  // Generate storyboard
  const sections = config.storyboard
    ? config.storyboard(name, tagline, features, pricing)
    : [
        { timeRange: '00:00 - 00:05', visual: `Bold text zoom onto ${name}`, audio: fillTemplate(pickRandom(HOOK_TEMPLATES), vars) },
        { timeRange: '00:05 - 00:18', visual: `Show ${name} interface demo`, audio: `It's called ${name}, the ${tagline}. Just describe what you want and the AI does the rest in seconds.` },
        { timeRange: '00:18 - 00:35', visual: `Feature showcase: ${features[0]}, ${features[1]}`, audio: `It handles ${features[0].toLowerCase()} and ${features[1].toLowerCase()}. Whether you're a beginner or a pro, this saves hours of work.` },
        { timeRange: '00:35 - 00:48', visual: `Show more features and use cases`, audio: `If you're a ${audience.split(',')[0].trim().toLowerCase()}, you need to add this to your workflow today.` },
        { timeRange: '00:48 - 00:60', visual: `Pricing + CTA arrow pointing down`, audio: `${pricing} Link in the description. Subscribe for daily AI tool reviews!` }
      ];

  // Generate 4 viral titles
  const shuffledTitles = [...TITLE_TEMPLATES].sort(() => Math.random() - 0.5);
  const titles = shuffledTitles.slice(0, 4).map(t => fillTemplate(t, vars));

  // Tags with #Shorts and trending hashtags
  const tags = [
    'Shorts', 'AITools', 'AI', 'TechReview',
    name.replace(/\s+/g, ''), category.replace(/\s+/g, ''),
    'Productivity', 'FreeAI', 'ArtificialIntelligence',
    'TechTok', 'SaaS', 'Coding', 'Automation'
  ];

  // SEO-optimized description with hashtags
  const hashtagBlock = tags.slice(0, 8).map(t => `#${t}`).join(' ');
  const description = `${fillTemplate(pickRandom(HOOK_TEMPLATES), vars)}

🔗 Try ${name} FREE: ${affiliate}

${name} is the ${tagline}. In this Short, I show you exactly how it works and why ${config.role} are switching to it.

✅ What ${name} does:
${features.map(f => `• ${f}`).join('\n')}

💰 Pricing: ${pricing}

👉 Subscribe for daily AI tool reviews!
👇 Drop a comment: would you use this?

${hashtagBlock}`;

  return {
    productName: name,
    category,
    titles,
    description,
    tags,
    storyboard: sections
  };
}

export function formatScriptToMarkdown(scriptData) {
  let md = `# YouTube Shorts Script & Storyboard: ${scriptData.productName}\n\n`;
  
  md += `## 💡 Proposed Metadata\n`;
  md += `### Suggested Titles (Hook-Optimized):\n`;
  scriptData.titles.forEach((t, i) => {
    md += `${i + 1}. **${t}**\n`;
  });
  md += `\n`;
  md += `### Tags:\n\`${scriptData.tags.join(', ')}\`\n\n`;
  
  md += `### Video Description:\n`;
  md += `\`\`\`text\n${scriptData.description}\n\`\`\`\n\n`;
  
  md += `## 🎬 Visual Storyboard & Audio Script (60s Limit)\n`;
  md += `| Time | 🎥 Visual Cues | 🎙️ Narration |\n`;
  md += `| :--- | :--- | :--- |\n`;
  
  scriptData.storyboard.forEach(s => {
    md += `| **${s.timeRange}** | ${s.visual} | *"${s.audio}"* |\n`;
  });
  
  md += `\n---\n*Generated by YouTube SaaS Reviewer v2.0 — Hook-First Engine*`;

  return md;
}
